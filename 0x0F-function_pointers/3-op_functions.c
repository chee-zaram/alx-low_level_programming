/**
 * op_add - Adds two numbers.
 * @a: First number to add.
 * @b: Second number to add.
 *
 * Return: Returns the sum of the two numbers.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts one number from the other.
 * @a: Number to subtract from.
 * @b: Number to subtract.
 *
 * Return: Difference of the two numbers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two numbers.
 * @a: Number to multiply.
 * @b: Number to multiply by.
 *
 * Return: Product of the two numbers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides a number by another.
 * @a: Number to be divided.
 * @b: Divisor.
 *
 * Return: Result of dividing a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Gets the remainder of the division of two numbers.
 * @a: Number to be divided.
 * @b: Divisor.
 *
 * Return: The remainder after dividing a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

